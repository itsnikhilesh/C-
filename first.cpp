#include <iostream> 
using namespace std; 
  
// Function that returns the 
// multiplication of two numbers 
// a and b 
int multiply(int a, int b) 
{ 
    return a * b; 
} 
  
// Driver Code 
int main() 
{ 
    int a = 2, b = 4; 
  
    // Function Call 
    cout << multiply(a, b); 
  
    return 0; 
}